#include <iostream>

using namespace std;

//Функция НОД по алгоритму Евклида
int NOD(int a, int b) {
	while (a != b) {
		if (a > b) a = a - b;
		else b = b - a;
	}
	return a;
}

int main() {
	setlocale(LC_ALL, "rus");	
	int a, b;
	cout << "Введите целые положительные числа:" << endl;
	cin >> a >> b;
	cout << "Их наибольший общий делитель: " << NOD(a, b);
}
